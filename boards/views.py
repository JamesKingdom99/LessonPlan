def signup(request):
    if request.method == 'POST':
        form = UserCreationForm(request.POST)
        if form.is_valid():
            user = form.save()
            auth_login(request, user)
            return redirect('blog')
    else:
        form = UserCreationForm()
    return render(request, 'signup.html', {'form': form})

